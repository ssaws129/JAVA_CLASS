public class _04_DoWhile {

	public static void main(String[] args) {
		/*
		 * 
		 */
	int num1 = 6;
	do {
		System.out.println(num1); // 현재 숫자 출력
		num1++;
	}
	while(num1<=5);
	/*
	 * while 과 do while 의 차이점
	 * while 은 조건이 false 라면 한 번도 실행되지 않음
	 * do while : 조건이 false 라도 최소 1번 실행됨
	 *	
	 */			
	
	
	
		
		
		
		
		
		
	}

}
